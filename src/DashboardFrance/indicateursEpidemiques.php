<p>Afin de prendre des mesures de déconfinement le 11 mai dernier, le gouvernement s'est basé sur deux indicateurs.</p>
<p><!-- /wp:paragraph --></p>
<p><!-- wp:paragraph --></p>
<p>Le premier indicateur est la circulation de virus. Il correspond à la proportion de personnes se présentant aux urgences pour une suspicion de Covid19 par rapport au nombre total de personnes se présentant aux urgences.</p>
<p><!-- /wp:paragraph --></p>
<p><!-- wp:paragraph --></p>
<p>Le deuxième indicateur est la saturation des services de réanimation. Il correspond au nombre de lits de réanimation occupés par rapport au nombre de lits (avant l'épidémie).</p>
